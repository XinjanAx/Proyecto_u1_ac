package com.uce.demo.consultorio;

public class Paciente {
	private String nombre;
	private int edad;
	
//------get-set------------------------------
	public String getNombre() {
		return nombre;
	}
	public int getEdad() {
		return edad;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	

}
